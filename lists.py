# creating a list of friends
friends = ["Joyce", "Eric", "Lewis", "Tim"]
print(friends)
# printing a value in a list based of their index
print(friends[2])
# indexing backwards
print(friends[-3])
# prints from index one and the others after that
print(friends[1:])
# prints in the specified range (up to but not including 3)
print(friends[1:3])
# Modify a list
friends[3] = "Timothy"
print(friends[3])

